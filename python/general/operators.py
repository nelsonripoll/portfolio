# addition
2 + 3 # 5

# subtraction
3 - 2 # 1

# multiplication
2 * 3 # 6

# division
3 / 2 # 1.5

# python 2.7 
# 3   / 2   = 1
# 3.0 / 2   = 1.5 
# 3   / 2.0 = 1.5
# 3.0 / 2.0 = 1.5

# modulus (remainder after division)
4 % 2 # 0 (4 / 2 is 2 with no remainder)
7 % 3 # 1 (7 / 3 is 2 with a remainder of 1)

# floor division (quotient after remainder is removed)
9//2 # 4 (9 / 2 is 4 with a remainder of 1)
6//3 # 2 (6 / 3 is 2 with no remainder)

# exponents
3 ** 2 # 9

# floats
0.1 + 0.2  # 0.3
2 * 0.2    # 0.4

# order of operation
2 + 3 * 4            # 14
(2 + 3) * 4          # 20
(4 - 1) * 3 + 2 * 4  # 17 

# greater than (equal to)
3 > 2
3 >= 3

# less than (equal to)
2 < 3
2 <= 3

# equal to
2 == 2

# not equal to
3 != 2

# assignment
total   = 3 + 5 # assign 8 to total
total  += 1 # total = 9
total  -= 1 # total = 8
total  *= 2 # total = 16
total  /= 2 # total = 8
total  %= 3 # total = 2
total **= 3 # total = 8
total //= 2 # total = 4

# bitwise
a = 60 # 0011 1100
b = 13 # 0000 1101

# binary AND (bit exists in both)
a & b  # 0000 1100

# binary OR (bit exists in either)
a | b  # 0011 1101

# binary XOR (bit exists in one but not the other)
a ^ b  # 0011 0001
